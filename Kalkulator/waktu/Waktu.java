package Kalkulator.waktu;

public class Waktu {
    private double time;

    public Waktu(double time) {
        this.time = time;
    }

    public double getTime() {
        return time;
    }

    public void setTime(double time) {
        this.time = time;
    }

    public double toDetik() {
        return time;
    }

    public double toMenit() {
        return time;
    }

    public double toJam() {
        return time;
    }

    public double toHari() {
        return time;
    }

    public double toMinggu() {
        return time;
    }
}
